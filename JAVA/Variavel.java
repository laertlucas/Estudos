public class Variavel {
	public static void main (String[] args){
		/* Variaveis sempre iniciar com letras minusculas. 
		 * Não iniciam com numeros.
		 * Não iniciar com caracteres, apesar de serem permitidos.
		 * Não utilizar espaço na variavel
		 * segundaPalavra maiusculas (camel-case)
		 * Nomes significativos, para não complicar nas manutenções futuras.
		*/
		String nome = "Lucas"; 
		int idade = 23;
		boolean casado = true;
		
		System.out.println("Texto");
		System.out.println(nome);
		System.out.println(idade);
		System.out.println(casado);
	}
}